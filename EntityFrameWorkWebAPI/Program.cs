
using EntityFrameWorkWebAPI.Models;
using EntityFrameWorkWebAPI.Repos;
using Microsoft.EntityFrameworkCore;

namespace EntityFrameWorkWebAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddDbContext<EmpDbContext>(db => db.UseSqlServer(builder.Configuration.GetConnectionString("ConStr")));
            builder.Services.AddScoped<IEmployeeService, EmployeeRepository>();
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
